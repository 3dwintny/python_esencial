# Slices en Pythobn 
""" 
Python tien una des las sintaxis más poderosas para manipular secuencias 
Esta sintaxis se llama slice (Rebanad en español)
    - Secuenci[comienzo:final:pasos]
    
    - Ejemplo
    
        my_name = 'David'
        my_name[0]
        my_name[-1]
        my_name[0:3]
        my_name[::2]
"""

my_name = 'edwintonymejiapaxtor'
print(my_name[0])
print(my_name[0:3])
print(my_name[::4])
print(my_name[3:3])
print(my_name[3:])
print(my_name[:])
print(my_name[1:-1:2])